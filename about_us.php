		<h3>Introduction </h3>
<p>Indian Institute of Tourism and Travel Management (IITTM) is an autonomous body under Ministry of Tourism, Government of India and is one of the premier institutes in the country offering education, training, research and consultancy in sustainable management of tourism, travel and other allied sectors. The stakeholdersâ€™ realization that the country is in need of such professionals who can provide an excellent standard of products and services, resulted in the creation of IITTM in 1983 at New Delhi. It is devoted to the pursuit of higher knowledge in tourism and its dissemination to a diverse audience. Over the years, Institute has established a distinguished identity of its own and reached at a commanding position among sectoral B-Schools in the country. With its focus in meeting the changing needs of the tourism industry, it has played a pioneering role in the propagation and professionalisation of tourism education. The management of the Institute is entrusted in the hands of the Board of Governors having the Union Minister for Tourism as its Chairperson. The efforts of the Institute have been instrumental in evolving the fundamental framework for tourism education and training in the country as it exists today. The Ministry of Tourism, Government of India has also entrusted the institute to conduct regional level guide training programmes for all the regions of the country.</p>

<p>Having set the benchmark in Tourism sector, we have begun the journey to define excellence in field of ever expanding Service sector. IITTM, with its vast resource pool and enormous experience will set trends and create new paths which will set the pattern to be followed in future.</p>

<h3>The Mission</h3>
<p>IITTM is committed to developing quality human resources for tourism and allied services. The target groups of its educational/ training programmes extend much beyond the organized sectors of the economy. IITTM endeavours to nurture professional and managerial excellence, social and cultural sensitivity, moral and ethical responsibility with concern for the environment and strive for latest techniques to develop decision making abilities with a resolute approach towards productivity, excellence, innovation and value for others to enable its participants to keep pace with the changing scenario of the economy and its environs.</p>

<h3>The Culture</h3>
<p>The faculty, staff, students and the administration of the Institute contribute to the building up an organizational culture marked by team spirit, confidence, mutual respect and concern for others. The students of the regular courses and the participants of various training programmes enrich the culture of the Institute with their diverse background and experience. IITTM welcomes people working at different levels to attend MDPs and various tailor-made training programmes in IITTM centres at Gwalior, Bhubaneswar, New Delhi, Goa and Nellore as well as in other parts of the country.</p>
        
